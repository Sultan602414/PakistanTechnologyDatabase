CREATE TABLE Telecommunication_Stats(TSID INT NOT NULL, Cellular_Mobile INT, Wireless INT, Year YEAR, PRIMARY KEY (TSID));

CREATE TABLE SoftwareHouse( SoftwareHouse_ID INT NOT NULL, Name VARCHAR(255), About VARCHAR(5000), SoftwareCompany INT, PRIMARY KEY (SoftwareHouse_ID));

CREATE TABLE Telecommunication (Telecommunication_id INT NOT NULL, TSID INT, SoftwareHouse_ID INT, Month VARCHAr(100), Jazz FLOAT(24), Ufone FLOAT(24), Telenor FLOAT(24),Warid FLOAT(24),Zong FLOAT(24), PRIMARY KEY (Telecommunication_id), FOREIGN KEY (TSID ) REFERENCES Telecommunication_Stats(TSID ), FOREIGN KEY (SoftwareHouse_ID ) REFERENCES SoftwareHouse(SoftwareHouse_ID)); 

CREATE TABLE SoftwareDevelopmentCompanies ( SDC_ID INT NOT NULL, Company_Name VARCHAR(255), Rating FLOAT(4,2), Location VARCHAR(500), PRIMARY KEY (SDC_ID ));

CREATE TABLE SoftwareProject(SoftwareHouse_ID INT NOT NULL, SDC_ID INT NOT NULL, Project VARCHAR(255), PRIMARY KEY (SoftwareHouse_ID , SDC_ID), FOREIGN KEY (SoftwareHouse_ID) REFERENCES SoftwareHouse(SoftwareHouse_ID), FOREIGN KEY (SDC_ID ) REFERENCES SoftwareDevelopmentCompanies (SDC_ID)); 

CREATE TABLE Clients(CompanyClient_ID INT NOT NULL, SDC_ID INT, Client_Name VARCHAR(255), PRIMARY KEY (CompanyClient_ID), FOREIGN KEY (SDC_ID ) REFERENCES SoftwareDevelopmentCompanies (SDC_ID));

CREATE TABLE Freelancers(Freelancer_ID INT NOT NULL, Name VARCHAR(255), Profession VARCHAR(1000), Earning FLOAT(4,2), Reviews VARCHAR(50), Hour_Rate VARCHAR(50), PRIMARY KEY(Freelancer_ID));

CREATE TABLE FreelancerCompanyAssignment(FreelancerCompany_ID INT NOT NULL, Company_ID INT, Freelancer_ID INT, Payment FLOAT(10,2), PRIMARY KEY(FreelancerCompany_ID), FOREIGN KEY (Freelancer_ID ) REFERENCES Freelancers(Freelancer_ID), FOREIGN KEY (Company_ID  ) REFERENCES SoftwareDevelopmentCompanies (SDC_ID));

